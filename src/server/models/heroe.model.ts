export class Spider {
    nombre: string;
    id: string;
    bio: string;
    imagen: string;
    aparicion: string;
    casa: string;

    constructor(nombre:string, id:string, bio:string, imagen:string, aparicion:string, casa:string){
        this.nombre = nombre;
        this.id = id;
        this.bio = bio;
        this.imagen = imagen;
        this.aparicion = aparicion;
        this.casa = casa;

    }
}
